export function getCategoryBySlug(slug: string): BestForCategoryDef | undefined {
  return BEST_FOR_CATEGORIES.find((c) => c.slug === slug);
}

export function getSeasonalFeatured(): BestForCategoryDef {
  const month = new Date().getMonth() + 1;
  const seasonal = BEST_FOR_CATEGORIES
    .filter((c) => c.seasonalMonths.includes(month) && c.tileSize !== "standard")
    .sort((a, b) => a.sortOrder - b.sortOrder);
  return seasonal[0] || BEST_FOR_CATEGORIES[0];
}

export function searchCategories(query: string): BestForCategoryDef[] {
  const q = query.toLowerCase();
  return BEST_FOR_CATEGORIES.filter(
    (c) => c.label.toLowerCase().includes(q) ||
           c.searchKeywords.some((kw) => kw.includes(q))
  );
}
